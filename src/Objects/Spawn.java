package Objects;

/*
    Contiene las coordenadas donde se dibujara por primera vez el personaje
*/
public class Spawn {
    // atributos
    int x, y;
    
    public Spawn (int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
        
}

